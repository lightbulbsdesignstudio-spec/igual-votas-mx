export type SeccionLookupResult =
  | { found: true; distrito: number; codigo: string }
  | { found: false; reason: "not_cdmx" | "invalid_seccion" };

export function lookupDistrictBySeccion(seccion: string): SeccionLookupResult {
  const num = parseInt(seccion, 10);
  if (isNaN(num) || num < 1) {
    return { found: false, reason: "invalid_seccion" };
  }
  const distrito = SECCION_TO_DISTRITO[num];
  if (distrito === undefined) {
    return { found: false, reason: "not_cdmx" };
  }
  return {
    found: true,
    distrito,
    codigo: `CDMX-${distrito.toString().padStart(2, '0')}`,
  };
}